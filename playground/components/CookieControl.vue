<template>
  <client-only>
    <section v-if="cookies.text" class="cookieControl">
      <transition :name="`cookieControl__Bar--${cookies.barPosition}`">
        <div
          v-if="colorsSet && !cookies.consent"
          :class="`cookieControl__Bar cookieControl__Bar--${cookies.barPosition}`"
        >
          <div class="cookieControl__BarContainer">
            <div>
              <slot name="bar">
                <h3 v-text="cookies.text.barTitle" />
                <p v-text="cookies.text.barDescription" />
              </slot>
            </div>
            <div class="cookieControl__BarButtons">
              <button
                @click="cookies.modal = true"
                v-text="cookies.text.manageCookies"
              />
              <button
                @click="setConsent({ reload: false })"
                v-text="cookies.text.acceptAll"
              />
            </div>
          </div>
        </div>
      </transition>
      <button
        v-if="cookies.controlButton && colorsSet && cookies.consent"
        class="cookieControl__ControlButton"
        aria-label="Cookie control"
        @click="cookies.modal = true"
      >
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512">
          <path
            fill="currentColor"
            d="M510.52 255.82c-69.97-.85-126.47-57.69-126.47-127.86-70.17 0-127-56.49-127.86-126.45-27.26-4.14-55.13.3-79.72 12.82l-69.13 35.22a132.221 132.221 0 00-57.79 57.81l-35.1 68.88a132.645 132.645 0 00-12.82 80.95l12.08 76.27a132.521 132.521 0 0037.16 72.96l54.77 54.76a132.036 132.036 0 0072.71 37.06l76.71 12.15c27.51 4.36 55.7-.11 80.53-12.76l69.13-35.21a132.273 132.273 0 0057.79-57.81l35.1-68.88c12.56-24.64 17.01-52.58 12.91-79.91zM176 368c-17.67 0-32-14.33-32-32s14.33-32 32-32 32 14.33 32 32-14.33 32-32 32zm32-160c-17.67 0-32-14.33-32-32s14.33-32 32-32 32 14.33 32 32-14.33 32-32 32zm160 128c-17.67 0-32-14.33-32-32s14.33-32 32-32 32 14.33 32 32-14.33 32-32 32z"
          />
        </svg>
      </button>
      <transition name="cookieControl__Modal">
        <div v-if="cookies.modal" class="cookieControl__Modal">
          <p
            v-if="!saved"
            class="cookieControl__ModalUnsaved"
            v-text="cookies.text.unsaved"
          />
          <div class="cookieControl__ModalContent">
            <div>
              <slot name="modal" />
              <button
                class="cookieControl__ModalClose"
                @click="cookies.modal = false"
                v-text="cookies.text.close"
              />
              <div
                v-for="cookieType in ['necessary', 'optional']"
                :key="cookieType"
              >
                <h3 v-text="cookies.text[cookieType]" />
                <ul>
                  <li v-for="cookie in cookies[cookieType]" :key="cookie.id">
                    <div class="cookieControl__ModalInputWrapper">
                      <input
                        v-if="
                          cookieType === 'necessary' &&
                          cookie.name !== 'functional'
                        "
                        :id="getCookieFirstName(cookie.name)"
                        type="checkbox"
                        disabled
                        checked
                      />
                      <input
                        v-else
                        :id="getCookieFirstName(cookie.name)"
                        type="checkbox"
                        :checked="
                          cookies.enabledList.includes(
                            cookie.identifier ||
                              cookies.slugify(getCookieFirstName(cookie.name))
                          ) ||
                          (cookies.get('cookie_control_consent').length === 0 &&
                            cookie.initialState === true)
                        "
                        @change="toogleCookie(cookie)"
                      />
                      <label :for="getCookieFirstName(cookie.name)">
                        {{ getName(cookie.name) }}
                      </label>
                      <span class="cookieControl__ModalCookieName">
                        {{ getName(cookie.name) }}
                        <span v-if="cookie.description">
                          {{ getDescription(cookie.description) }}
                        </span>
                      </span>
                    </div>
                    <template v-if="cookie.cookies">
                      <slot name="cookie" v-bind="{ config: cookie }">
                        <ul>
                          <li v-for="item in cookie.cookies" :key="item.id">
                            {{ item }}
                          </li>
                        </ul>
                      </slot>
                    </template>
                  </li>
                </ul>
              </div>
              <div class="cookieControl__ModalButtons">
                <button
                  @click="setConsent({ type: 'partial' })"
                  v-text="cookies.text.save"
                />
                <button
                  @click="() => setConsent({})"
                  v-text="cookies.text.acceptAll"
                />
                <button
                  @click="setConsent({ declineAll: true })"
                  v-text="cookies.text.declineAll"
                />
              </div>
            </div>
          </div>
        </div>
      </transition>
    </section>
  </client-only>
</template>

<script lang="ts">
import { defineComponent, onBeforeMount, reactive, watch } from 'vue'

import { useNuxtApp } from '#app'

import { I18n, State } from '../../src/types'

export default defineComponent({
  name: 'CookieControl',
  props: {
    locale: {
      default: 'en',
      type: String,
    },
  },
  setup(props) {
    const nuxtApp = useNuxtApp()
    const $cookies = nuxtApp.$cookies as State

    const data = reactive({
      saved: true,
      colorsSet: false,
      cookies: $cookies,
    })
    const computations = {
      expirationDate() {
        const date = new Date()
        date.setFullYear(date.getFullYear() + 1)
        return date.toUTCString()
      },

      optionalCookies() {
        return data.cookies.optional
      },
    }
    const methods = {
      toogleCookie(cookie) {
        const cookieName =
          cookie.identifier ||
          data.cookies.slugify(methods.getCookieFirstName(cookie.name))
        if (data.saved) data.saved = false
        if (!data.cookies.enabledList.includes(cookieName))
          data.cookies.enabledList.push(cookieName)
        else
          data.cookies.enabledList.splice(
            data.cookies.enabledList.indexOf(cookieName),
            1
          )
      },

      setConsent({
        type = undefined,
        consent = true,
        reload = true,
        declineAll = false,
      }: {
        type?: 'partial'
        consent?: boolean
        reload?: boolean
        declineAll?: boolean
      }) {
        data.cookies.set({
          name: 'cookie_control_consent',
          value: consent,
          expires: computations.expirationDate,
        })
        const enabledCookies = declineAll
          ? []
          : type === 'partial' && consent
          ? data.cookies.enabledList
          : [
              ...computations
                .optionalCookies()
                .map(
                  (c) =>
                    c.identifier ||
                    data.cookies.slugify(methods.getCookieFirstName(c.name))
                ),
            ]
        data.cookies.set({
          name: 'cookie_control_enabled_cookies',
          value: consent ? enabledCookies.join(',') : '',
          expires: computations.expirationDate,
        })
        if (!reload) {
          data.cookies.setConsent()
          this.$cookies.modal = false
        } else window.location.reload()
      },

      getDescription(description) {
        if (typeof description === 'string')
          return ` ${
            data.cookies.dashInDescription !== false ? '-' : ''
          } ${description}`
        else if (description[props.locale])
          return ` ${data.cookies.dashInDescription !== false ? '-' : ''} ${
            description[props.locale]
          }`
        return ''
      },

      getName(name) {
        return name === 'functional'
          ? data.cookies.text.functional
          : typeof name === 'string'
          ? name
          : name[props.locale]
          ? name[props.locale]
          : name[Object.keys(name)[0]]
      },

      getCookieFirstName(name) {
        return typeof name === 'string' ? name : name[Object.keys(name)[0]]
      },

      setTexts: async (isChanged = false) => {
        let text: I18n | undefined

        try {
          text = (await import(`#nuxtCookieControl/locale/${props.locale}`))
            .default
        } catch (e) {
          text = (await import(`#nuxtCookieControl/locale/en`)).default
        }

        if (
          text &&
          data.cookies.text &&
          Object.keys(data.cookies.text).length > 0
        ) {
          if (data.cookies.text.locale) {
            Object.assign(text, data.cookies.text.locale[props.locale])
          }
          if (!isChanged) Object.assign(text, data.cookies.text)
        }

        $cookies.text = text
        // this.$set($cookies, 'text', text)
      },
    }

    onBeforeMount(async () => {
      methods.setTexts()
      if (data.cookies.colors) {
        const variables = {}
        for (const key in data.cookies.colors) {
          const k = key.toLowerCase().includes('unactive')
            ? key.replace(/Unactive/g, 'Inactive')
            : key
          variables[`cookie-control-${k}`] = `${data.cookies.colors[key]}`
        }

        if (data.cookies.cssPolyfill) {
          const module = await import('css-vars-ponyfill')
          const cssVars = module.default
          cssVars({ variables })
        } else {
          for (const cssVar in variables) {
            document.documentElement.style.setProperty(
              `--${cssVar}`,
              variables[cssVar]
            )
          }
        }
      }

      if (
        !data.cookies.get('cookie_control_consent') ||
        data.cookies.get('cookie_control_consent').length === 0
      ) {
        computations.optionalCookies().forEach((c) => {
          if (c.initialState === true) {
            data.cookies.enabledList.push(
              c.identifier ||
                data.cookies.slugify(methods.getCookieFirstName(c.name))
            )
          }
        })
      }

      data.colorsSet = true
    })

    watch(
      () => props.locale,
      (_currentValue, _oldValue) => {
        methods.setTexts(true)
      }
    )

    return {
      ...data,
      ...computations,
      ...methods,
    }
  },
})
</script>
